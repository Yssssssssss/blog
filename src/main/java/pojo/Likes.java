package pojo;

public class Likes {
    private Integer likesId;

    private Integer likesUserid;

    private Integer likesTextid;

    public Integer getLikesId() {
        return likesId;
    }

    public void setLikesId(Integer likesId) {
        this.likesId = likesId;
    }

    public Integer getLikesUserid() {
        return likesUserid;
    }

    public void setLikesUserid(Integer likesUserid) {
        this.likesUserid = likesUserid;
    }

    public Integer getLikesTextid() {
        return likesTextid;
    }

    public void setLikesTextid(Integer likesTextid) {
        this.likesTextid = likesTextid;
    }
}